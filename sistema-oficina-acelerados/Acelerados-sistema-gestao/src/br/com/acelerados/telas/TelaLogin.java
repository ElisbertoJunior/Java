    public void logar() {
        String sql = "select * from dbusers where login=? and senha=?";
        try {
            //as linhas abaixo pereparam a consulta ao banco em funcao do 
            //que foi digitado nas caixas de texto. O ? e substiuido pelo 
            //consteudo das variaveis
            pst = conexao.prepareStatement(sql);
            pst.setString(1, txtUsuario.getText());
            String captura = new String(txtSenha.getPassword());
            pst.setString(2, captura);
            //a linha abaixo executa a query
            rs = pst.executeQuery();
            //se existir usuario e senha correspondente
            if (rs.next()) {
                //a linha abaixo obtem o conteudo do campo perfil da tabela dbusers
                String perfil = rs.getString(6);
                String usuario = rs.getString(2);
                // a estrutura baixo faz o tratamento do perfil do usuario
                if (perfil.equals("admin")) {
                    TelaPrincipal principal = new TelaPrincipal();
                    principal.setVisible(true);
                    TelaPrincipal.menRel.setEnabled(true);
                    TelaPrincipal.menCadUsu.setEnabled(true);
                    TelaPrincipal.lblUsuario.setText(usuario);
                    TelaPrincipal.lblUsuario.setForeground(Color.red);
                    this.dispose();
                    conexao.close();
                } else {
                    TelaPrincipal principal = new TelaPrincipal();
                    principal.setVisible(true);
                    TelaPrincipal.lblUsuario.setText(usuario);
                    this.dispose();
                    conexao.close();

                }
            } else {
                JOptionPane.showMessageDialog(null, "Usuario e/ou senha invalido(a)");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);

        }
    }

